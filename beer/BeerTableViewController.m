@interface BeerTableViewController ()

@end

@implementation BeerTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    self.beers = [[NSMutableArray alloc] init];
    [self loadInitialData];

}

- (void)loadInitialData {
    Beer *beer1 = [[Beer alloc] init];
    beer1.name = @"bernardus";
    
    [self.beers addObject:beer1];
    
    Beer *beer2 = [[Beer alloc] init];
    beer2.name = @"trapist";
    
    [self.beers addObject:beer2];
    
    Beer *beer3 = [[Beer alloc] init];
    beer3.name = @"calorus";

    [self.beers addObject:beer3];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    // Return the number of rows in the section.
    return [self.beers count];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"ListPrototypeCell" forIndexPath:indexPath];
    
    
    Beer *beer = [self.beers objectAtIndex:indexPath.row];
    cell.textLabel.text = beer.name;

    
    return cell;
}


